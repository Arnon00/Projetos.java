package Aulas;

import java.util.Scanner;

public class Aula021 {
	
	public static void main (String [] args){
		
		//estrutura while
		
		 int  x = 0;
		 int  v = 10;	 
		 
		 while (x <= 10){   //contagem crecente
			  System.out.println(x);
			    x = x + 1;
		 }
		 
		 while (v >= 0){   //contagem decrecente
			  System.out.println(v);
			    v = v - 1;
		 }
		 
	}

}
